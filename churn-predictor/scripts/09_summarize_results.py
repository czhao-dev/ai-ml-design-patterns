#!/usr/bin/env python3
"""Regenerate reports/results_summary.md from the CSV outputs of scripts
01, 07, and 08.

Usage:
    python scripts/09_summarize_results.py
"""

from __future__ import annotations

import sys
from pathlib import Path

import pandas as pd

sys.path.insert(0, str(Path(__file__).resolve().parents[1]))

from src import config


def _df_to_markdown(df: pd.DataFrame, float_format: str = "{:.3f}") -> str:
    formatted = df.copy()
    for col in formatted.select_dtypes(include=["float", "float64"]).columns:
        formatted[col] = formatted[col].map(lambda v: float_format.format(v))
    return formatted.to_markdown(index=False)


def main() -> None:
    lines = ["# Results Summary", "", "_Generated by `scripts/09_summarize_results.py`._", ""]

    balance_path = config.REPORTS_DIR / "covariate_balance.csv"
    if balance_path.exists():
        balance = pd.read_csv(balance_path)
        healthy = bool((balance["smd"] < 0.1).all())
        lines += [
            "## Randomization check",
            "",
            f"All observed covariates have |SMD| < 0.1 across treatment/control: **{healthy}**. "
            "Top covariates by imbalance:",
            "",
            _df_to_markdown(balance.head(10)),
            "",
        ]

    comparison_path = config.REPORTS_DIR / "uplift_model_comparison.csv"
    if comparison_path.exists():
        comparison = pd.read_csv(comparison_path)
        lines += [
            "## Uplift model comparison (held-out test set)",
            "",
            "`qini_coefficient` and `auuc` are computed against real held-out RCT "
            "outcomes -- higher is better, 0 means no better than random targeting.",
            "",
            _df_to_markdown(comparison),
            "",
            "![Qini curves](figures/qini_curves.png)",
            "",
        ]

    revenue_path = config.REPORTS_DIR / "revenue_policy_comparison.csv"
    if revenue_path.exists():
        revenue = pd.read_csv(revenue_path)
        lines += [
            "## $ net incremental revenue by targeting policy",
            "",
            "`net_revenue = realized incremental spend in the targeted group - "
            "(cost_per_offer x customers targeted)`, computed from real "
            "treatment/control spend differences on held-out RCT data.",
            "",
            _df_to_markdown(revenue),
            "",
        ]

    config.RESULTS_SUMMARY_PATH.parent.mkdir(parents=True, exist_ok=True)
    config.RESULTS_SUMMARY_PATH.write_text("\n".join(lines))
    print(f"Wrote {config.RESULTS_SUMMARY_PATH}")


if __name__ == "__main__":
    main()
